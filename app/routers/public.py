@router.get("/", response_class=HTMLResponse)
async def home(request: Request):
    featured_posts = BLOG_POSTS[:3]
    return templates.TemplateResponse(
        "public/home.html",
        {
            "request": request,
            "page_title": "Music Memoire — персональные песни в подарок",
            "featured_posts": featured_posts,
        },
    )


@router.get("/blog", response_class=HTMLResponse)
async def blog_index(request: Request):
    return templates.TemplateResponse(
        "public/blog_index.html",
        {
            "request": request,
            "page_title": "Блог — Music Memoire",
            "posts": BLOG_POSTS,
        },
    )


@router.get("/blog/{slug}", response_class=HTMLResponse)
async def blog_post(slug: str, request: Request):
    post = next((item for item in BLOG_POSTS if item["slug"] == slug), None)
    if post is None:
        raise HTTPException(status_code=404, detail="Статья не найдена.")

    return templates.TemplateResponse(
        "public/blog_post.html",
        {
            "request": request,
            "page_title": f'{post["title"]} — Music Memoire',
            "post": post,
        },
    )


@router.get("/health")
async def health():
    return {"ok": True, "service": "musicmemoire-web"}
